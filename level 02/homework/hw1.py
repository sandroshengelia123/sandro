# and / or
print(True and True)            # true - ორივე პირობა სიმართლეა
print(True and False)           # false - ერთი მაინც რომ იყოს false შედეგი იქნება ფალსე
print(False and True)           # false - იგივე მიზეზით
print(False and False)          # false - ორივე პირობა მცდარია


print(True or True)             # true - ერთი პირობა მაინც
print(True or False)            # true - მინიმუმ ერთი პირობა
print(False or True)            # true - იგივე მიზეზი
print(False or False)           # false - ორივე პირობა



