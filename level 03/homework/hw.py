# 8) მომხმარებელს შემოატანინეთ ტექსტი და დაითვალეთ სიმბოლოების რაოდენობა
text_input = input("შეიყვანეთ ტექსტი: ")

# სიმბოლოების რაოდენობა
print(f"სიმბოლოების რაოდენობა: {len(text_input)}")


# 9) მომხმარებელს შემოატანინეთ რიცხვი და დიაპაზონი გაავლეთ 0-დან ამ რიცხვამდე
number_input = int(input("შეიყვანეთ რიცხვი: "))

# დიაპაზონის გამოყვანა
for i in range(number_input + 1):
    print(i)


# for loop არის ციკლი, რომელიც გამოიყენება ერთნაირი ოპერაციების განმეორებით შესასრულებლად.
# range() ფუნქცია ქმნის რიცხვთა დიაპაზონს და გამოიყენება for loop-თან ერთად.
# range(start, stop, step) არის დიაპაზონი, რომელიც იწყება start-დან, მთავრდება stop-ზე და ნაბიჯი არის step.

# მაგალითი:
for i in range(5):  # შექმნის რიცხვებს 0-დან 4-მდე
    print(i)


# 11) მომხმარებელს შემოატანინეთ სტრინგი
text_input = input("შეიყვანეთ სტრინგი: ")

# სტრინგის გაბრუნება for loop-ის საშუალებით
reversed_text = ""
for char in text_input:
    reversed_text = char + reversed_text  # თითოეული სიმბოლო წინამორბედს უმატებს

# პალინდრომის შემოწმება
if text_input == reversed_text:
    print(f"{text_input} არის პალინდრომი!")
else:
    print(f"{text_input} არ არის პალინდრომი.")
