# 3) მომხმარებელს შემოატანინეთ სახელი და რიცხვი. რამდენჯერაც რიცხვი, იმდენჯერ დააბეჭდოს ტექსტი
name_input = input("შეიყვანეთ თქვენი სახელი: ")
times_input = int(input("შეიყვანეთ რიცხვი: "))

# ტექსტის გამოქვნა მითითებული რაოდენობით
for _ in range(times_input):
    print(name_input)


# 4) მომხმარებელს შემოატანინეთ რიცხვი და 1-დან ამ რიცხვის ჩათვლით 5-ის გამოტოვებით ყველა რიცხვი
number_input = int(input("შეიყვანეთ რიცხვი: "))

# ყველა რიცხვი 1-დან number_input ჩათვლით 5-ის გამოტოვებით
for i in range(1, number_input + 1):
    if i % 5 != 0:
        print(i)


# 5) მომხმარებელს შემოატანინეთ რიცხვი და 2-მდე უკუსვლით გამოიტანეთ ყველაფერი
number_input = int(input("შეიყვანეთ რიცხვი: "))

# რიცხვიდან 2-მდე უკუსვლით
for i in range(number_input, 1, -1):
    print(i)


# 6) მომხმარებელს შემოატანინეთ რიცხვი და დაპრინტე ამ რიცხვის ჩათვლით ყველაფრის ჯამი
number_input = int(input("შეიყვანეთ რიცხვი: "))

